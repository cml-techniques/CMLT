            {
                e.Row.Cells[4].Visible = false;
                e.Row.Cells[5].Visible = false;
            }
            if(e.Row.RowType==DataControlRowType.DataRow)
            {
                e.Row.Cells[0].Text = (e.Row.DataItemIndex + 1).ToString();
                _qty += Convert.ToDecimal(e.Row.Cells[2].Text);
                _pcom += Convert.ToDecimal(e.Row.Cells[3].Text);

                _percomplete += Convert.ToDecimal(e.Row.Cells[7].Text);

                if (lblprj.Text == "ASAO" || lblprj.Text == "ASAO1")
                {
                    _com += Convert.ToDecimal(e.Row.Cells[4].Text);
                    _acce += Convert.ToDecimal(e.Row.Cells[5].Text);
                    _sumry += (Convert.ToDecimal(e.Row.Cells[7].Text) * Convert.ToDecimal(e.Row.Cells[2].Text));
                }
                _rows += 1;
            }
            if (e.Row.RowType == DataControlRowType.Header)
            {
                if (lblprj.Text == "ASAO" || lblprj.Text == "ASAO1")
                {
                    e.Row.Cells[1].Text = "SYSTEMS";
                    e.Row.Cells[3].Text = "TOTAL PRE-COMMISSIONED";
                }
            }
            if (e.Row.RowType == DataControlRowType.Footer)
            {
                e.Row.Cells[1].Text = "TOTAL";
                e.Row.Cells[2].Text = _qty.ToString();
                e.Row.Cells[3].Text = _pcom.ToString();
                e.Row.Cells[4].Text = _com.ToString();
                e.Row.Cells[5].Text = _acce.ToString();
                if (lblprj.Text == "ASAO" || lblprj.Text == "ASAO1")
                    e.Row.Cells[6].Text = Decimal.Round(_sumry / _qty).ToString() + '%';
                else if (lblprj.Text == "MOE" || lblprj.Text == "MOE1")
                    e.Row.Cells[6].Text = Decimal.Round(_percomplete / _rows, MidpointRounding.AwayFromZero).ToString() + '%';
