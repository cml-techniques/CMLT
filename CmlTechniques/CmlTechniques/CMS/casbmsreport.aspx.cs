                        _pcom = (_pcom / _qty) * 100;
                        if (_pcom >= 99.5m && _pcom < 100m) _pcom = 99;
                        e.Row.Cells[6].Text = Decimal.Round(_pcom).ToString() + '%';
                    }
                    else
                        e.Row.Cells[6].Text = "0%";
                }
            }

