        private void Generate_Summary()
        {
            try
            {
                DataTable _dtsummary = new DataTable();
                _dtsummary.Columns.Add("SYS_NAME", typeof(string));
                _dtsummary.Columns.Add("QTY", typeof(string));
                _dtsummary.Columns.Add("PLANNED_COMPLETED", typeof(string));
                _dtsummary.Columns.Add("TESTED", typeof(string));
                _dtsummary.Columns.Add("WITNESSED", typeof(string));
                _dtsummary.Columns.Add("PLANNED_PROGRESS", typeof(string));
                _dtsummary.Columns.Add("ACTUAL_PROGRESS", typeof(string));
                _dtsummary.Columns.Add("CODE", typeof(string));
                _dtsummary.Columns.Add("COUNT", typeof(string));
                _dtsummary.Columns.Add("PCDCOUNT", typeof(string));
                _dtsummary.Columns.Add("type", typeof(string));
                var distinctRows = (from DataRow dRow in _dtresult.Rows
                                    select new { col1 = dRow["Sys_Id"], col2 = dRow["Sys_name"], col3 = dRow["Cat"], col4 = dRow["type"] }).Distinct();
                foreach (var row in distinctRows)
                {
                    decimal _p1 = 0;
                    decimal _p2 = 0;
                    decimal _p3 = 0;
                    decimal _p_p1 = 0;
                    decimal _actualprogress = 0;
                    decimal _plannedprogress = 0;
                    decimal count = 0;
                    decimal pcount = 0;

                    decimal _qty = 0;
                    var _result = from _data in _dtresult.AsEnumerable()
                                  where _data.Field<int>("Sys_id") == Convert.ToInt32(row.col1.ToString())
                                  select _data;
                    foreach (var _row in _result)
                    {
                        if (Convert.ToDecimal(_row["per_com2"]) != -1)
                        {
                            _p2 += Convert.ToDecimal(_row["per_com2"].ToString());
                            pcount += 1;
                        }

                        if (Convert.ToDecimal(_row["per_com3"]) != -1)
                            _p3 += 1;
                        if (Convert.ToDecimal(_row["per_com1"]) != -1)
                        {
                            _p1 += Convert.ToDecimal(_row["per_com1"].ToString());
                            count += 1;
                        }                            
                        var todaysDate = DateTime.Today;
                        if (!string.IsNullOrEmpty(hdnpcd.Value))
                        {
                            todaysDate = DateTime.ParseExact(hdnpcd.Value, "dd/MM/yyyy", System.Globalization.CultureInfo.InvariantCulture);
                        }
                        if (_row["pcdate"] != null && _row["pcdate"].ToString() != "")
                        {
                            DateTime pcdate = DateTime.ParseExact(_row["pcdate"].ToString(), "dd/MM/yyyy", System.Globalization.CultureInfo.InvariantCulture);
                            if (pcdate <= todaysDate) _p_p1 += 1;
                        }
                        _qty += 1;
                    }
                    if (count > 0) _actualprogress = Decimal.Round((_p1 / count) * 100, MidpointRounding.AwayFromZero);
                    if (_p3 > 0) _plannedprogress = Decimal.Round((_p_p1 / _p3) * 100, MidpointRounding.AwayFromZero);

                    if (lblprj.Text != "ARSD")
                    {
                        if (row.col4.ToString() == "fusible") if (pcount > 0) _actualprogress = Decimal.Round((_p2 / pcount) * 100, MidpointRounding.AwayFromZero);
                    }

                    DataRow _drow = _dtsummary.NewRow();
                    _drow["SYS_NAME"] = row.col2.ToString();
                    _drow["QTY"] = _qty.ToString();
                    _drow["PLANNED_COMPLETED"] = Decimal.Round(_p_p1).ToString();
                    _drow["TESTED"] = Decimal.Round(_p1).ToString();
                    _drow["WITNESSED"] = Decimal.Round(_p2).ToString();
                    _drow["PLANNED_PROGRESS"] = _plannedprogress.ToString();
                    _drow["ACTUAL_PROGRESS"] = _actualprogress.ToString();
                    _drow["CODE"] = row.col3.ToString();
                    _drow["COUNT"] = count.ToString();
                    _drow["PCDCOUNT"] = _p3.ToString();
                    _drow["type"] = row.col4.ToString();
                    _dtsummary.Rows.Add(_drow);
                }
                mygridsumm.DataSource = _dtsummary;
                mygridsumm.DataBind();
                _summary = _dtsummary;
            }
            catch (Exception ex)
            {
                ScriptManager.RegisterStartupScript(this, typeof(string), "close", "alert('" + ex.Message + "');", true);
            }
        }
