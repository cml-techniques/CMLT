            Load_Master();

            var _filter = from o in _dtfilter.AsEnumerable()
                          where (_el1 == "All" || o.Field<string>("B_Z") == _el1) && (_el2 == "All" || o.Field<string>("Cat") == _el2) && (_el3 == "All" || o.Field<string>("F_lvl") == _el3) && (_el4 == "All" || o.Field<string>("F_from") == _el4) && (_el5 == "All" || o.Field<string>("Loc") == _el5) && (_el6 == "All" || o.Field<decimal>("per_com4") == Convert.ToDecimal(_el6))
